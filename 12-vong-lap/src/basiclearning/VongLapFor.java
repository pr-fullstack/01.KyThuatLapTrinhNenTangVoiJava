package basiclearning;

public class VongLapFor {
    public static void main(String[] args) {
        int i = 0;
        for (; i < 5; i++) {
            System.out.println(i);
        }
        // Bước 1: Khởi tạo i = 0 => thỏa mãn => Xuất 0
        // Bước 2: i = 1 => thỏa mãn => Xuất 1
        // Bước 3: i = 2 => thỏa mãn => Xuất 2
        // Bước 4: i = 3 => thỏa mãn => Xuất 3
        // Bước 5: i = 4 => thỏa mãn => Xuất 4
        // Bước 6: i = 5 => không thỏa mãn =>
    }
}
